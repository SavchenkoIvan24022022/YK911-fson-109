/**
 * Оператори
 *
 * - Оператори порівняння > < >= <=
 * - Оператори рівності == === != !==
 * - Приведення типів операндів
 */

console.log('5 > 4:', 5 > 4);

console.log("10 >= '7':", 10 >= '7');

console.log("'2' > '12':", '2' > '12');

console.log("'2' < '12':", '2' < '12');

console.log("'4' == 4:", '4' == 4); // 4 == 4

console.log("'6' === 6:", '6' === 6); // string === number

console.log("'false' === false:", 'false' === false); // string === boolean

console.log('1 == true:', 1 == true);

console.log('1 === true:', 1 !== true); // number === boolean

console.log("'0' == false:", '0' == false); // 0 == 0

console.log("'0' === false:", '0' === false);

console.log("'Papaya' < 'papaya':", 'Papaya' < 'papaya'); // 80 < 112

console.log("'papaya' === 'papaya':", 'papaya' === 'papaya');

console.log('undefined == null:', undefined == null);

console.log('undefined === null:', undefined === null);
